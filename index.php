            <div class="row">
                    <div class="col-lg-5 col-xs-12">
                        <p class="orange">
                            A Câmara de Comércio, desenvolvimento & integração Brasil ECOWAS, é uma entidade civil e de direito privado,
                            reconhecida oficialmente pelos paises membros da ECOWAS.
                        </p>
                        <p class="orange">
                            A comunidade Econômica dos Estados da África 
                            Ocidental(CEDEAO / ECOWAS) é um grupo regional de 15 países da 
                            África Ocidental. Fundada em 25 de maio 1975, 
                            com a assinatura do Tratado de Lagos, a sua
                            missão é promover a integração econômica
                            na região.
                        </p>
                    </div>
                    <div class="col-lg-7 col-xs-12">
                        <p>Considerado um dos pilares da Comunicação Econômica da Africa, a organização foi
                        fundada para alcançar a “autossuficiênciia coletiva” de seus Estados menbros, criando
                        um único grande bloco comercial através de uma união econômica e comercial.
                        </p>
                        <p>                
                            Ela também serve como uma força para a paz na região. A região opera oficialmente 
                            em três idiomas com o mesmo nível de importância: Frânces, Inglês e  Português.

                        </p>
                        <p>
                            Alguns menbros da organização têm entrado e saído dela ao longo dos anos. Em 1976,
                            Cabo Verde entrou para a ECOWAS e em dezembro de 2000 a Mauritânia saiu, depois de
                            ter anunciado sua intenção de saíde do bloco em dezembro de 1999.
                        </p>
                    </div>
